// Helper functions for components to use
export const t = (lang: string, key: string, subKey?: string) => {
  const langData = TRANSLATIONS[lang] || TRANSLATIONS["en"];
  if (subKey) {
    return (
      langData.steps?.[key]?.[subKey] || TRANSLATIONS["en"].steps[key][subKey]
    );
  }
  return langData[key] || TRANSLATIONS["en"][key];
};

export const getOptionLabel = (
  lang: string,
  optionId: string,
  stepId: string
) => {
  const langData = TRANSLATIONS[lang] || TRANSLATIONS["en"];
  const englishList = TRANSLATIONS["en"].options[stepId];
  const index = englishList.indexOf(optionId);
  if (index === -1) return optionId;
  return langData.options?.[stepId]?.[index] || optionId;
};
